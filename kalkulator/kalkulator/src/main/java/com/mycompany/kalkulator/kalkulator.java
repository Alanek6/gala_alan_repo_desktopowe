    private void jButtonRownaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRownaActionPerformed
       
        liczba2 = Double.parseDouble(jTextFieldObliczenia.getText()); 
        
        switch(wybordzialania){
           case 1:
               wynik = liczba1 + liczba2;
               jTextFieldObliczenia.setText(wynik + "");
               break;
           case 2:
               wynik = liczba1 - liczba2;
               jTextFieldObliczenia.setText(wynik + "");
               break;
           case 3:
               wynik = liczba1 * liczba2;
               jTextFieldObliczenia.setText(wynik + "");
               break;
           case 4:                    
               if(liczba1 == 0|| liczba2 == 0){
                   jTextFieldObliczenia.setText(wynik + "0");
               }
               else{
                    wynik = liczba1 / liczba2;
                    jTextFieldObliczenia.setText(wynik + "");
                    break;       
               }
           case 5:
               wynik = Math.pow(liczba1,liczba2);
                jTextFieldObliczenia.setText(wynik + "");         
        }        
               
        wybordzialania = 0;
        
    }//GEN-LAST:event_jButtonRownaActionPerformed

